package day5_operators;

import java.sql.SQLOutput;

public class UnaryOperators {

    public static void main(String[] args) {

        int
                a=25;

        System.out.println(++a);
        System.out.println(--a);

        int
                b=30;

        System.out.println(b++);
        System.out.println(b);
        System.out.println(b--);
        System.out.println(b);
    }
}
