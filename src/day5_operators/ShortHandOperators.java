package day5_operators;

public class ShortHandOperators {

    public static void main(String[] args) {

        int a = 20;

        System.out.println(a);

        a/=15;

        System.out.println(a);

        a*=20;

        System.out.println(a++);

        a-=20;

        System.out.println(a);

    }
}
