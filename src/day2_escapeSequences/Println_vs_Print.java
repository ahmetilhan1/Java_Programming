package day2_escapeSequences;

public class Println_vs_Print {

    public static void main(String[] args) {

        System.out.println("\tHello Everyone!");
        System.out.println("\tHow are you Today?");

        System.out.println("------------------------");

        System.out.print("Hello Everyone!");
        System.out.print("How are you Today?");




    }
}
/*  Println will allow you to get a new line for the next code
    Print will not and list every print on the same line
    'serr' for error message

 */